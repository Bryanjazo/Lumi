const SKIN_SOURCES: Record<string, typeof BASE> = {
  default: BASE,
  original: BASE,
  cream: CREAM,
  plum: PLUM,
  moss: MOSS,
  terra: TERRA,
  mist: MIST,
  midnight: MIDNIGHT,
};

/**
 * Returns the require()'d asset for a given mood + skin. Unknown
 * skin ids fall back to the base cat; unknown moods fall back to
 * idle. Second arg defaults to 'default' so old call sites keep
 * working (they render the original cat).
 */
export const lunaSource = (
  mood: LunaMood = 'idle',
  skinId: string = 'default',
) => {
  const skin = SKIN_SOURCES[skinId] ?? BASE;
  return skin[mood] ?? skin.idle;
};

/**
 * Reactive hook — returns the user's currently-selected avatar id
 * so components can pass it to lunaSource without threading it
 * through props. Re-renders when the user picks a new skin in the
 * profile picker.
 */
export const useLunaSkin = (): string => {
  return useUserStore((s) => s.avatar) || 'default';
};
