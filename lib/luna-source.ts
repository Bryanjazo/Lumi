// Single source of truth for Luna's animated GIFs.
//
// Each mood + skin maps to its own asset so callers (paywall, Home
// nook, Me room, auth screens, onboarding, profile, chat avatar,
// etc.) can render the right expression + skin with one consistent
// API. The skin defaults to the user's currently-chosen avatar via
// the `useLunaSkin()` hook.
//
// Use:
//   import { lunaSource, useLunaSkin } from '../lib/luna-source';
//   const skin = useLunaSkin();
//   <Image source={lunaSource(mood, skin)} style={{ width, height }} />
//
// When in doubt, default to 'idle' + 'default' (original tan cat).

import { useUserStore } from '../store/userStore';

export type LunaMood = 'idle' | 'happy' | 'sad' | 'sleep' | 'walk' | 'lick';

// `require` returns a numeric asset id at bundle time — Metro
// statically resolves these, so we keep one require per (skin, mood)
// combo and pick at runtime. New moods or skins get a new asset +
// a new branch.
//
// 'walk' is the animated GIF that shows the cat MOVING on its
// little feet — used by the Me-tab pacing loop. 'lick' is the
// grooming animation; we inject it as a brief beat during task
// completion + focus start + occasional Me-tab idle moments so the
// cat reads as a living creature with personality quirks instead
// of just rotating through static emotions.
